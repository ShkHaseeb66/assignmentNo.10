//diameter
function diameter(r){
    return 2*r
}
console.log(diameter(14));

//circumference
function  circumference(r){
    return 2*3.14*r
}
console.log( circumference(20));
//circle
function circle(r){
    return 3.14*r*r
}
console.log(circle(19));